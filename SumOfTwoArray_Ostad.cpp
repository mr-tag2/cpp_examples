#include <iostream>
using namespace std;
int main()
{
    int i, j;
    int a[3][4], b[3][4], c[3][4];
    for (i = 0; i <= 2; i++)
        for (j = 0; j <= 3; j++)
        {
            cout << "enter a number" << i << "," << j << ":";
            cin >> a[i][j];
        }
    //-----------------------------------------
    for (i = 0; i <= 2; i++)
    {
        cout << "\n";
        for (j = 0; j <= 3; j++)
            cout << a[i][j] << "   ";
    }
    //----------------------------------------------
    for (i = 0; i <= 2; i++)
        for (j = 0; j <= 3; j++)
        {
            cout << "enter b number" << i << "," << j << ":";
            cin >> b[i][j];
        }
    //-----------------------------------------------
    for (i = 0; i <= 2; i++)
    {
        cout << "\n";
        for (j = 0; j <= 3; j++)
            cout << b[i][j] << "   ";
    } //---------------------------------------------------
    for (i = 0; i <= 2; i++)
        for (j = 0; j <= 3; j++)
        {
            c[i][j] = a[i][j] + b[i][j];
        }
    //---------------------------------------------------
    for (i = 0; i <= 2; i++)
    {
        cout << "\n";
        for (j = 0; j <= 3; j++)
            cout << c[i][j] << "   ";
    }
    return 0;
}