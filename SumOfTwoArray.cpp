#include <iostream>
using namespace std;

int main()
{

    int i, ix,
        a[2][3], b[2][3], c[2][3];

    for (i = 0; i < 2; i++)
        for (ix = 0; ix < 3; ix++)
        {
            cout << "enter a number " << i << " - " << ix << ":";
            cin >> a[i][ix];
        }

    //-----------------------------------------
    cout << "===============================================================\n";
    //-----------------------------------------

    for (i = 0; i < 2; i++)
        for (ix = 0; ix < 3; ix++)
        {
            cout << "enter b number " << i << " - " << ix << ":";
            cin >> b[i][ix];
        }

    //-----------------------------------------
    cout << "===============================================================";
    //-----------------------------------------

    for (i = 0; i < 2; i++)
        for (ix = 0; ix < 3; ix++)
        {
            c[i][ix] = a[i][ix] + b[i][ix];
            cout << "\nsum number  " << i << " - " << ix << ":  " << c[i][ix];
        }

    //-----------------------------------------
    return 0;
}
